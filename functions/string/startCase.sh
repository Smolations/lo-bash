
# _.startCase([string=''])

# Converts string to start case.

# Arguments
# [string=''] (string): The string to convert.
# Returns
# (string): Returns the start cased string.

# Example
# _.startCase('--foo-bar');
# // → 'Foo Bar'

# _.startCase('fooBar');
# // → 'Foo Bar'

# _.startCase('__foo_bar__');
# // → 'Foo Bar'

function _.startCase {
  local turn=0

  return $turn
}
